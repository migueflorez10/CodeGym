# Operadores relacionales

# Igual que 
a = 1 ==2 
print(a)

# Distinto de
b = 3!= 2
print(b)

# mayor que
c = 3 > 4
print(c)

# Menor que 
d = 3 < 1
print(d)

# Mayor o igual que
e = 3 >= 3
print(e)

# Menor o igual que
f = 3 <= 2
print(f)

# ejemplos
l1 = [1,2,3]
l2 = [1,5,4]

print(len(l1) == len(l2))
print(l1[0] == l2[0])