# Operadores de asignacion

# ASIGNACIÓN BÁSICA
x = 10
print("=  ->", x)      # x vale 10


# SUMAR Y GUARDAR
x += 5
print("+= ->", x)      # x = 10 + 5 = 15


# RESTAR Y GUARDAR
x -= 3
print("-= ->", x)      # x = 15 - 3 = 12


# MULTIPLICAR Y GUARDAR
x *= 2
print("*= ->", x)      # x = 12 * 2 = 24


# DIVIDIR Y GUARDAR
x /= 4
print("/= ->", x)      # x = 24 / 4 = 6.0 (float)


# DIVISIÓN ENTERA Y GUARDAR
x //= 2
print("//= ->", x)     # x = 6.0 // 2 = 3.0


# MÓDULO (RESIDUO) Y GUARDAR
x %= 2
print("%= ->", x)      # x = 3.0 % 2 = 1.0
