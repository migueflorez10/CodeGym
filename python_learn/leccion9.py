# Estructuras de control

n = 0 
while n <= 10: # mientras n sea menor-igual que 10, repite el codigo
    if (n % 2) == 0: # si el resudio de la division entre n y 2 es 0, toma la siguiente desicion
        print(n, 'es un numero par') # cumple
    else:
        print(n, 'es un numero impar') # no cumple
    n = n + 1.  # aumenta n en 1 
    
    