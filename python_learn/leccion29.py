# Funciones integradas de python

# Transformar una cadena de texto a numero entero
n = int('10')
print(n)

# Transformar una cadena de texto a numero flotante 
f = float('10.5')
print(f)

# Concatenar cadenas de caracteres + enteros 
c = 'Miguel Angel Martinez ' + str(24) + ' years'
print(c)

# Transformar un numero a binario
print(bin(10))

# Transformar un numero a hexadencimal
print(hex(10))

# Transformar un numero binario a entero 
print(int('0b1010', 2))

# Transformar un numero hexadecimal a entero
print(int('0xa', 16))

# Obtener el valor absoluto de un numero 
print(abs(-40))

# Obtener el redonde de un numero
print(round(18.6))

# Ejecutar código que está escrito como texto (string)
n = 10
print(eval('n * 5 + 8'))

# Saber la longitud de una cadena, lista, coleccion, etc
print(len('HolaMundo'))
print(len([]))

